    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $district = new District();
        $district->name = $request->district_name;

        $upazilas = explode(',',$request->upazila_name);
        $district->save();

        $lastInsertedDistrictId = $district->id;


        foreach($upazilas as $key=> $item)
        {
            $upazila = new Upazila();
            $upazila->name = $item;
            $upazila->district_id = $lastInsertedDistrictId;
            $upazila->save();
        }
        return back();
    }

